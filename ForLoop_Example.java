package basics;

public class ForLoop_Example {

	public static void main(String[] args) {
		//print numbers from 0 to 100
		//for CTRL + Spacebar
		for (int i = 0; i <=100; i++) {
			System.out.println("Numbers are : "+i);
		}
		//step 1 : Initialization --int i=0;
		//step 2 : condition -- i<100
		//step 3 : if step2 is true then it will execute code inside for loop
		//step 4 : Increment or decrement -- i++ --- incrase i Value by 1
	}

}
